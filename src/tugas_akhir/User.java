    private void lanjutkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lanjutkanActionPerformed
        // TODO add your handling code here:
          
          
        try {
            Connection con = (Connection) Koneksi.getConnection();
            int baris = jTable1.getRowCount();
            
            for (int i = 0; i < baris; i++) {
                String sql = "INSERT INTO penjualanrinci(tanggal,NoFaktur, id_pelanggan, jenis_ikan, jumlah, harga, total) VALUES('"+ txtanggal.getText() +"','"+ txNoTransaksi.getText() +"','"
                        + txpelanggan.getText() +"','"+ jTable1.getValueAt(i, 1) +"','"+ jTable1.getValueAt(i, 2) 
                        +"','"+ jTable1.getValueAt(i, 3) +"','"+ jTable1.getValueAt(i, 4) 
                        +"')";
                
                try (PreparedStatement pst = con.prepareStatement(sql)) {
                    pst.executeUpdate();
                }
            }
                    
                    JOptionPane.showMessageDialog(null, "Berhasil menyimpan");
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Gagal menyimpan" + e.getMessage());
        }
        clear();
        utama();
      
        kosong();
        txtampil.setText("Rp. 0");
    }//GEN-LAST:event_lanjutkanActionPerformed
    
