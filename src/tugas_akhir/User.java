public final void table(){
    
        DefaultTableModel tbl = new DefaultTableModel();
         tbl.addColumn("ID Ikan");
        tbl.addColumn("Jenis Ikan");
        tbl.addColumn("Harga Ikan");
        
        try {
            Statement st = (Statement) Koneksi.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tb_ikan");
            
            while (rs.next()){
                tbl.addRow(new Object[]{
                 rs.getString("id_ikan"),
                rs.getString("jenis_ikan"),
                rs.getString("harga_ikan")
                
                        
                        
                });
            
            jTable2.setModel(tbl);
        } 
        
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Koneksi Data gagal");
                
                }
        
    }

    public void totalBiaya(){
        int jumlahBaris = jTable1.getRowCount();
        int totalBiaya = 0;
        int jumlahBarang, hargaBarang;
        for (int i = 0; i < jumlahBaris; i++) {
            jumlahBarang = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
            hargaBarang = Integer.parseInt(jTable1.getValueAt(i, 3).toString());
            totalBiaya = totalBiaya + (jumlahBarang * hargaBarang);
        }
        btnbayar.setText(String.valueOf(totalBiaya));
        txtampil.setText("Rp "+ totalBiaya +",00");
    }
    
